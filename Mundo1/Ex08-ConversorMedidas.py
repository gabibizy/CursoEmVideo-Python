# 08 - Conversor de medidas
n1 = float(input('Digite o valor em metros: '))
print(f'O valor de {n1}m corresponde a: ') 
print(f'Kilômetros: {n1 / 1000}km')
print(f'Hectómetros: {n1 / 100}hm')
print(f'Decâmetros: {n1 / 10}dam')
print(f'Decímetros: {n1 * 10}dm')
print(f'Centimetros: {n1 * 100}cm')
print(f'Milímetros: {n1 * 1000}mm')
