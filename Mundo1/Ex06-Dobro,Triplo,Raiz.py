# 06 - Dobro, Triplo e Raiz Quadrada
from math import sqrt

n1 = int(input('Digite um número: '))
dobro = n1 * 2
triplo = n1 * 3
raiz = sqrt(n1)
print(f'O dobro de {n1} é {dobro}, o triplo é {triplo} e a raiz quadrada é {raiz:.2f}')


